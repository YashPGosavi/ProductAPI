def clean_text(text):
    """
    Clean and preprocess text data.
    """
    # Example: Convert text to lowercase
    cleaned_text = text.lower()
    return cleaned_text

def format_price(price):
    """
    Format price to a desired format.
    """
    # Example: Round price to 2 decimal places
    formatted_price = round(price, 2)
    return formatted_price

